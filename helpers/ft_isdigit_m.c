#include "minishell.h"

int	ft_isdigit_m(int n)
{
	if (n < 10)
		return (n >= 0 && n <= 9);
	else
	{
		while (n > 9)
		{
			ft_isdigit_m(n / 10);
			return (n >= 0 && n <= 9);
		}
	}
	return (0);
}

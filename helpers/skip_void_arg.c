#include "minishell.h"

int	skip_void_arg(t_token **t_l)
{
	if ((*t_l)->value[0] == '\0' && (*t_l)->quotes == QUOTE_NONE)
	{
		(*t_l) = (*t_l)->next;
		return (1);
	}
	return (0);
}
